try:
    # Получить ввод пользователя как строку без пробелов
    user_input = input("Введите строку без пробелов: ").replace(" ", "")

    # Создать множество из ввода пользователя
    char_set = set(user_input)

    # Вывести созданное множество
    print("Созданный набор:")
    print(char_set)

except Exception as e:
    print("Произошла ошибка:", e)